package fr.eseo.cc3.model.exception;

public class ExceptionAlimentationVm extends Exception {
	private final String vmName;
	private final int codeErreur;
	
	
	
	public ExceptionAlimentationVm(String vmName, int codeErreur) {
		super();
		this.vmName = vmName;
		this.codeErreur = codeErreur;
	}



	public String getVmName() {
		return vmName;
	}



	public int getCodeErreur() {
		return codeErreur;
	}
	
	
	
	
}
